let num = 10.510;

//round the specified number to the next integer number
console.log(Math.ceil(num));
//round the specified number to the previous integer number
console.log(Math.floor(num));
//round the specified number to the nearest integer number
console.log(Math.round(num));

//return the largest number
console.log(Math.max(1, 2, 3, 4, 5, 6));
//return the smallest number
console.log(Math.min(1, 2, 3, 4, 5, 6));

//return a random number between 1 and 6
console.log(Math.round(Math.random() * (1 - 6) + 6));

//return PI value
console.log(Math.PI);

//return an exponentiation
console.log(Math.pow(8, 2));
console.log(8 ** 2);